package com.simplilearnforloop;

public class ForLoop1 {

	public static void main(String[] args) {
		// For loop is used to iterate a list
		// you must start with initialization, the condition and then the increment
		
      for (int i = 1; i <= 10; i++) {
    	  System.out.println("The i value: " + i);
	}
  }
}
